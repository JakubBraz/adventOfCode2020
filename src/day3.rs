pub fn part1() {
    let i = input();
    let path = reach_bottom(&i, 0, 0, (3, 1));
    let result: usize = path.iter().filter(|&&e| e == '#').count();
    println!("{:?}", result);
}

pub fn part2() {
    let inp = input();
    let slopes: [(usize, usize); 5] = [(1, 1), (3, 1), (5, 1), (7, 1), (1, 2)];
    let s: Vec<usize> = slopes.iter()
        .map(|&slope| reach_bottom(&inp, 0, 0, slope).iter()
            .filter(|&&x| x == '#').count()).collect();
    let result: usize = s.iter().product();
    println!("{:?}", result);
}

fn reach_bottom(ar: &Vec<Vec<char>>, x: usize, y: usize, slope: (usize, usize)) -> Vec<char> {
    if y >= ar.len() { return vec![]; }
    let elem: char = get_elem(ar, x, y);
    let mut temp = reach_bottom(ar, x + slope.0, y + slope.1, slope);
    temp.push(elem);
    return temp;
}

fn get_elem(ar: &Vec<Vec<char>>, x: usize, y: usize) -> char {
    ar[y][x % ar[0].len()]
}

